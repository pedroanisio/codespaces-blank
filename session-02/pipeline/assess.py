def _ffprobe_json(path: Path, entries: str = "format") -> dict:
    """Run ffprobe and return parsed JSON."""
    try:
        result = subprocess.run(
            ["ffprobe", "-v", "error", "-print_format", "json",
             "-show_format", "-show_streams", str(path)],
            capture_output=True, text=True, timeout=15,
        )
        return json.loads(result.stdout)
    except Exception as exc:
        log.warning("ffprobe_failed", path=str(path), error=str(exc))
        return {}


def _ffprobe_duration(path: Path) -> float | None:
    """Get duration in seconds from ffprobe."""
    data = _ffprobe_json(path)
    fmt = data.get("format", {})
    dur = fmt.get("duration")
    return float(dur) if dur else None


def _ffprobe_video_stream(path: Path) -> dict:
    """Get first video stream info."""
    data = _ffprobe_json(path)
    for s in data.get("streams", []):
        if s.get("codec_type") == "video":
            return s
    return {}


def _ffprobe_audio_stream(path: Path) -> dict:
    """Get first audio stream info."""
    data = _ffprobe_json(path)
    for s in data.get("streams", []):
        if s.get("codec_type") == "audio":
            return s
    return {}


