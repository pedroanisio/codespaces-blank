_TTY = sys.stdout.isatty()

def _c(code: str, t: str) -> str:
    return f"\033[{code}m{t}\033[0m" if _TTY else t

def _print_report(report: AssessmentReport) -> None:
    """Print a formatted assessment report to stdout."""
    PASS = lambda t: _c("32", t)
    FAIL = lambda t: _c("31", t)
    WARN = lambda t: _c("33", t)
    INFO = lambda t: _c("36", t)
    BOLD = lambda t: _c("1", t)
    DIM = lambda t: _c("2", t)

    print(f"\n{'═' * 70}")
    print(BOLD("  VIDEO ASSESSMENT REPORT"))
    print(f"{'═' * 70}")
    print(f"  Instance : {report.instance_path}")
    print(f"  Output   : {report.output_dir}")
    print(f"  Layers   : {report.layers_run}")
    print(f"  Time     : {report.elapsed_sec:.1f}s")
    print()

    current_layer = 0
    layer_names = {1: "TECHNICAL COMPLIANCE", 2: "CONTENT ANALYSIS", 3: "AI-AUGMENTED"}

    for check in report.checks:
        if check.layer != current_layer:
            current_layer = check.layer
            name = layer_names.get(current_layer, f"LAYER {current_layer}")
            print(f"\n{BOLD(f'── Layer {current_layer}: {name} ')}" + "─" * max(0, 48 - len(name)))

        if check.passed:
            sym = PASS("✓")
        elif check.severity == "warning":
            sym = WARN("!")
        elif check.severity == "info":
            sym = INFO("·")
        else:
            sym = FAIL("✗")

        name_str = check.name[:45]
        print(f"  {sym} {name_str:<46}", end="")

        if check.passed:
            if check.actual:
                print(DIM(f" {check.actual}"))
            else:
                print()
        else:
            if check.expected and check.actual:
                print(f" expected={check.expected}  actual={check.actual}")
            elif check.detail:
                print(f" {check.detail}")
            else:
                print()

        # Print detail for vision checks (truncated)
        if check.name.startswith("vision_adherence:") and check.detail:
            try:
                detail_data = json.loads(check.detail)
                descs = detail_data.get("descriptions", [])
                if descs:
                    print(DIM(f"      → {descs[0][:120]}"))
                missing = detail_data.get("missing", [])
                if missing:
                    print(WARN(f"      → missing: {', '.join(missing[:5])}"))
            except json.JSONDecodeError:
                pass

        if check.name.startswith("unexpected_elements:") and not check.passed:
            print(WARN(f"      → {check.actual[:150]}"))

        if check.name.startswith("forbidden_violations:") and not check.passed:
            print(FAIL(f"      → {check.actual[:150]}"))

        if check.name.startswith(("environment_match:", "narrative_match:")) and not check.passed:
            if check.actual:
                print(WARN(f"      → {check.actual[:150]}"))

    print(f"\n{'═' * 70}")
    print(BOLD("  SUMMARY"))
    print(f"{'═' * 70}")
    print(f"  Total    : {len(report.checks)}")
    print(f"  {PASS('PASSED')}  : {report.passed}")
    print(f"  {FAIL('FAILED')}  : {report.failed}")
    print(f"  {WARN('WARNINGS')}: {report.warnings}")
    infos = sum(1 for c in report.checks if not c.passed and c.severity == "info")
    print(f"  {INFO('INFO')}    : {infos}")

    print()
    if report.failed == 0:
        print(PASS("  ✓ ALL CRITICAL CHECKS PASSED"))
    else:
        print(FAIL(f"  ✗ {report.failed} CRITICAL CHECK(S) FAILED"))
    print()


