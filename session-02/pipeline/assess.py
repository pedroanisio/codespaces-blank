def _extract_frame_at(video_path: Path, time_sec: float, out_path: Path) -> bool:
    """Extract a single frame at a given timestamp as PNG."""
    try:
        subprocess.run(
            ["ffmpeg", "-y", "-ss", str(time_sec), "-i", str(video_path),
             "-frames:v", "1", "-update", "1", str(out_path)],
            capture_output=True, timeout=15,
        )
        return out_path.exists() and out_path.stat().st_size > 0
    except Exception:
        return False


def _measure_loudness(audio_path: Path) -> dict:
    """Measure integrated loudness using ffmpeg loudnorm filter (2-pass measurement)."""
    try:
        result = subprocess.run(
            ["ffmpeg", "-i", str(audio_path), "-af",
             "loudnorm=print_format=json", "-f", "null", "-"],
            capture_output=True, text=True, timeout=30,
        )
        # loudnorm JSON is in stderr after the filter output
        stderr = result.stderr
        # Find the JSON block in stderr
        start = stderr.rfind("{")
        end = stderr.rfind("}") + 1
        if start >= 0 and end > start:
            return json.loads(stderr[start:end])
    except Exception as exc:
        log.debug("loudness_measure_failed", error=str(exc))
    return {}


def _detect_scene_changes(video_path: Path, threshold: float = 0.3) -> list[float]:
    """Detect scene changes using ffmpeg's select filter. Returns timestamps."""
    try:
        result = subprocess.run(
            ["ffmpeg", "-i", str(video_path), "-vf",
             f"select='gt(scene,{threshold})',showinfo",
             "-f", "null", "-"],
            capture_output=True, text=True, timeout=60,
        )
        import re
        timestamps = []
        for m in re.finditer(r"pts_time:(\d+\.?\d*)", result.stderr):
            timestamps.append(float(m.group(1)))
        return timestamps
    except Exception as exc:
        log.debug("scene_detect_failed", error=str(exc))
        return []


def _compute_ssim(path_a: Path, path_b: Path) -> float | None:
    """Compute SSIM between two video/image files. Returns mean SSIM."""
    try:
        result = subprocess.run(
            ["ffmpeg", "-i", str(path_a), "-i", str(path_b),
             "-lavfi", "ssim=stats_file=-", "-f", "null", "-"],
            capture_output=True, text=True, timeout=30,
        )
        import re
        # Look for "All:" SSIM value
        m = re.search(r"All:(\d+\.?\d*)", result.stderr)
        if m:
            return float(m.group(1))
    except Exception as exc:
        log.debug("ssim_failed", error=str(exc))
    return None


def _sample_color_histogram(video_path: Path, time_sec: float) -> dict[str, float] | None:
    """Sample a frame and return average RGB values (0-255)."""
    with tempfile.NamedTemporaryFile(suffix=".png", delete=False) as tmp:
        tmp_path = Path(tmp.name)
    try:
        if not _extract_frame_at(video_path, time_sec, tmp_path):
            return None
        result = subprocess.run(
            ["ffprobe", "-v", "error", "-f", "lavfi",
             "-i", f"movie={tmp_path},signalstats",
             "-show_entries", "frame_tags=lavfi.signalstats.YAVG,"
             "lavfi.signalstats.UAVG,lavfi.signalstats.VAVG",
             "-of", "csv=p=0"],
            capture_output=True, text=True, timeout=10,
        )
        # Fallback: use ffmpeg to get average pixel color
        result2 = subprocess.run(
            ["ffmpeg", "-i", str(tmp_path), "-vf",
             "scale=1:1:flags=area", "-pix_fmt", "rgb24",
             "-f", "rawvideo", "-"],
            capture_output=True, timeout=10,
        )
        if len(result2.stdout) >= 3:
            r, g, b = result2.stdout[0], result2.stdout[1], result2.stdout[2]
            return {"r": r, "g": g, "b": b}
    except Exception as exc:
        log.debug("color_sample_failed", error=str(exc))
    finally:
        tmp_path.unlink(missing_ok=True)
    return None


def _hex_to_rgb(hex_color: str) -> tuple[int, int, int]:
    h = hex_color.lstrip("#")
    if len(h) == 3:
        h = "".join(c * 2 for c in h)
    return (int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16))


def _color_distance(rgb_a: tuple[int, ...], rgb_b: tuple[int, ...]) -> float:
    """Euclidean distance in RGB space, normalized to 0-1."""
    return (sum((a - b) ** 2 for a, b in zip(rgb_a, rgb_b)) ** 0.5) / (3 * 255**2) ** 0.5


