def layer_1_technical(instance: dict, output_dir: Path, report: AssessmentReport) -> None:
    """Validate generated artifacts against spec using ffprobe."""

    production = instance.get("production", {})
    qp = (instance.get("qualityProfiles") or [{}])[0].get("profile", {})
    shots = _shots_in_order(instance)
    audio_assets = instance.get("assetLibrary", {}).get("audioAssets", [])
    target_runtime = _pick(instance, "project.targetRuntimeSec")
    tolerance = _pick(qp, "video.runtimeToleranceSec") or 0.5

    # ── 1.1 Shot file existence and duration ──
    shots_dir = output_dir / "shots"
    for shot in shots:
        sid = shot.get("logicalId") or shot.get("id") or ""
        expected_dur = shot.get("targetDurationSec")
        shot_path = shots_dir / f"{sid}.mp4"

        exists = shot_path.exists()
        report.add(CheckResult(
            name=f"shot_file_exists:{sid}",
            layer=1, passed=exists,
            expected=str(shot_path), actual="found" if exists else "missing",
        ))

        if exists and expected_dur:
            actual_dur = _ffprobe_duration(shot_path)
            if actual_dur is not None:
                # AI video generators often produce slightly different durations
                dur_ok = abs(actual_dur - expected_dur) <= max(1.0, expected_dur * 0.5)
                report.add(CheckResult(
                    name=f"shot_duration:{sid}",
                    layer=1, passed=dur_ok,
                    expected=expected_dur, actual=round(actual_dur, 2),
                    detail=f"tolerance ±{max(1.0, expected_dur * 0.5):.1f}s (generous for AI video)",
                    severity="warning",
                ))

    # ── 1.2 Audio file existence ──
    audio_dir = output_dir / "audio"
    for asset in audio_assets:
        aid = asset.get("logicalId") or asset.get("id") or ""
        audio_path = audio_dir / f"{aid}.mp3"
        if not audio_path.exists():
            audio_path = audio_dir / f"{aid}.flac"
        if not audio_path.exists():
            audio_path = audio_dir / f"{aid}.wav"

        exists = audio_path.exists()
        report.add(CheckResult(
            name=f"audio_file_exists:{aid}",
            layer=1, passed=exists,
            expected=aid, actual="found" if exists else "missing",
        ))

    # ── 1.3 Reference images (S13 protocol) ──
    refs_dir = output_dir / "references"
    characters = production.get("characters", [])
    environments = production.get("environments", [])

    expected_refs = []
    for char in characters:
        name = (char.get("logicalId") or char.get("id") or "").replace("char-", "")
        for view in ["front", "three_quarter", "full_body"]:
            expected_refs.append(f"char-{name}.{view}.png")
    for env in environments:
        name = (env.get("logicalId") or env.get("id") or "").replace("env-", "")
        for view in ["wide_plate", "detail_plate"]:
            expected_refs.append(f"env-{name}.{view}.png")

    for ref_name in expected_refs:
        ref_path = refs_dir / ref_name
        exists = ref_path.exists()
        report.add(CheckResult(
            name=f"reference_exists:{ref_name}",
            layer=1, passed=exists,
            expected=ref_name, actual="found" if exists else "missing",
            severity="warning",
        ))

    # ── 1.4 Intermediate files ──
    inter_dir = output_dir / "intermediate"
    for stage_name in ["01_op-concat.mp4", "02_op-audio-mix.mp4", "03_op-grade.mp4"]:
        stage_path = inter_dir / stage_name
        exists = stage_path.exists()
        report.add(CheckResult(
            name=f"intermediate_exists:{stage_name}",
            layer=1, passed=exists,
            expected=stage_name, actual="found" if exists else "missing",
            severity="warning",
        ))

    # ── 1.5 Final output or best available intermediate ──
    project_name = _pick(instance, "project.name") or "output"
    final_candidates = [
        output_dir / f"{project_name.lower()}.mp4",
        output_dir / "final.mp4",
        inter_dir / "03_op-grade.mp4",
        inter_dir / "02_op-audio-mix.mp4",
        inter_dir / "01_op-concat.mp4",
    ]
    final_path = None
    for candidate in final_candidates:
        if candidate.exists():
            final_path = candidate
            break

    if final_path is None:
        report.add(CheckResult(
            name="final_output_exists",
            layer=1, passed=False,
            expected="final video file", actual="no output found",
        ))
        return

    report.add(CheckResult(
        name="final_output_exists",
        layer=1, passed=True,
        expected="final video", actual=str(final_path.name),
    ))

    # ── 1.6 Final duration ──
    if target_runtime:
        final_dur = _ffprobe_duration(final_path)
        if final_dur is not None:
            dur_ok = abs(final_dur - target_runtime) <= tolerance
            report.add(CheckResult(
                name="final_duration",
                layer=1, passed=dur_ok,
                expected=f"{target_runtime}s ± {tolerance}s",
                actual=f"{final_dur:.2f}s",
            ))

    # ── 1.7 Video stream properties ──
    vstream = _ffprobe_video_stream(final_path)
    if vstream:
        # Resolution
        expected_w = _pick(qp, "video.resolution.widthPx")
        expected_h = _pick(qp, "video.resolution.heightPx")
        if expected_w and expected_h:
            actual_w = vstream.get("width")
            actual_h = vstream.get("height")
            # Pipeline normalises to 1920x1080 during concat
            report.add(CheckResult(
                name="video_resolution",
                layer=1,
                passed=(actual_w is not None and actual_h is not None),
                expected=f"{expected_w}x{expected_h}",
                actual=f"{actual_w}x{actual_h}" if actual_w else "unknown",
                detail="pipeline normalises to 1920x1080 at concat stage",
                severity="info",
            ))

        # Frame rate
        expected_fps = _pick(qp, "video.frameRate.fps")
        if expected_fps:
            r_frame_rate = vstream.get("r_frame_rate", "")
            if "/" in r_frame_rate:
                num, den = r_frame_rate.split("/")
                actual_fps = round(int(num) / int(den), 2) if int(den) else 0
            else:
                actual_fps = float(r_frame_rate) if r_frame_rate else 0
            fps_ok = abs(actual_fps - expected_fps) <= 1.0
            report.add(CheckResult(
                name="video_frame_rate",
                layer=1, passed=fps_ok,
                expected=f"{expected_fps} fps",
                actual=f"{actual_fps} fps",
            ))

        # Codec
        actual_codec = vstream.get("codec_name", "")
        report.add(CheckResult(
            name="video_codec",
            layer=1, passed=bool(actual_codec),
            expected="video codec present",
            actual=actual_codec or "none",
            severity="info",
        ))

    # ── 1.8 Audio stream properties ──
    astream = _ffprobe_audio_stream(final_path)
    if astream:
        expected_sr = _pick(qp, "audio.sampleRateHz")
        if expected_sr:
            actual_sr = int(astream.get("sample_rate", 0))
            sr_ok = actual_sr == expected_sr
            report.add(CheckResult(
                name="audio_sample_rate",
                layer=1, passed=sr_ok,
                expected=f"{expected_sr} Hz",
                actual=f"{actual_sr} Hz",
                severity="warning",
            ))

        expected_layout = _pick(qp, "audio.channelLayout")
        if expected_layout:
            actual_channels = int(astream.get("channels", 0))
            layout_map = {"mono": 1, "stereo": 2, "5.1": 6, "7.1": 8}
            expected_channels = layout_map.get(expected_layout, 0)
            ch_ok = actual_channels == expected_channels if expected_channels else True
            report.add(CheckResult(
                name="audio_channels",
                layer=1, passed=ch_ok,
                expected=f"{expected_layout} ({expected_channels}ch)",
                actual=f"{actual_channels}ch",
                severity="warning",
            ))


