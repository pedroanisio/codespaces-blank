def layer_2_content(instance: dict, output_dir: Path, report: AssessmentReport) -> None:
    """Content-level checks using ffmpeg analysis."""

    inter_dir = output_dir / "intermediate"
    concat_path = inter_dir / "01_op-concat.mp4"
    audio_mix_path = inter_dir / "02_op-audio-mix.mp4"
    grade_path = inter_dir / "03_op-grade.mp4"

    # Use the best available video for analysis
    analysis_path = None
    for p in [grade_path, audio_mix_path, concat_path]:
        if p.exists():
            analysis_path = p
            break
    if analysis_path is None:
        report.add(CheckResult(
            name="content_analysis_skipped",
            layer=2, passed=False,
            detail="no intermediate or final video found for content analysis",
        ))
        return

    shots = _shots_in_order(instance)
    timeline = ((instance.get("assembly") or {}).get("timelines") or [{}])[0]
    video_clips = timeline.get("videoClips", [])

    # ── 2.1 Scene cut detection ──
    # Compute expected cut points from timeline videoClips
    expected_cuts: list[float] = []
    for vc in video_clips[1:]:  # first clip starts at 0, cuts happen at each subsequent start
        start = vc.get("timelineStartSec")
        if start is not None:
            expected_cuts.append(float(start))

    detected_cuts = _detect_scene_changes(analysis_path, threshold=0.2)

    if expected_cuts and detected_cuts:
        # Match detected cuts to expected cuts (within 0.5s tolerance)
        matched = 0
        for expected in expected_cuts:
            for detected in detected_cuts:
                if abs(expected - detected) <= 0.5:
                    matched += 1
                    break
        match_ratio = matched / len(expected_cuts) if expected_cuts else 0
        report.add(CheckResult(
            name="scene_cut_detection",
            layer=2, passed=match_ratio >= 0.5,
            expected=f"{len(expected_cuts)} cuts at {expected_cuts}",
            actual=f"{len(detected_cuts)} detected, {matched} matched",
            detail=f"match ratio {match_ratio:.0%} (detected: {[round(t, 1) for t in detected_cuts[:15]]})",
            severity="warning",
        ))
    else:
        report.add(CheckResult(
            name="scene_cut_detection",
            layer=2, passed=True,
            detail="insufficient data for scene cut analysis",
            severity="info",
        ))

    # ── 2.2 Color palette adherence per shot ──
    shots_dir = output_dir / "shots"
    color_checks_done = 0
    color_checks_passed = 0

    for shot in shots:
        sid = shot.get("logicalId") or shot.get("id") or ""
        shot_path = shots_dir / f"{sid}.mp4"
        if not shot_path.exists():
            continue

        palette = _pick(shot, "cinematicSpec.style.palette")
        if not palette:
            continue

        # Sample color at the midpoint of the shot
        dur = _ffprobe_duration(shot_path) or 1.0
        avg_color = _sample_color_histogram(shot_path, dur / 2)
        if avg_color is None:
            continue

        color_checks_done += 1
        actual_rgb = (avg_color["r"], avg_color["g"], avg_color["b"])

        # Find closest palette color
        min_dist = 1.0
        closest = ""
        for hex_c in palette:
            pal_rgb = _hex_to_rgb(hex_c)
            dist = _color_distance(actual_rgb, pal_rgb)
            if dist < min_dist:
                min_dist = dist
                closest = hex_c

        # Generous threshold: within 60% distance is ok for AI video
        palette_ok = min_dist <= 0.6
        if palette_ok:
            color_checks_passed += 1

        report.add(CheckResult(
            name=f"color_palette:{sid}",
            layer=2, passed=palette_ok,
            expected=f"palette {palette}",
            actual=f"avg RGB({actual_rgb[0]},{actual_rgb[1]},{actual_rgb[2]}) closest={closest} dist={min_dist:.2f}",
            severity="warning",
        ))

    # ── 2.3 Audio loudness measurement ──
    if audio_mix_path.exists():
        loudness = _measure_loudness(audio_mix_path)
        if loudness:
            qp_profile = (instance.get("qualityProfiles") or [{}])[0].get("profile", {})
            target_lufs = _pick(qp_profile, "audio.loudnessIntegratedLUFS")
            actual_lufs = loudness.get("input_i")

            if target_lufs and actual_lufs:
                actual_lufs_f = float(actual_lufs)
                lufs_ok = abs(actual_lufs_f - target_lufs) <= 3.0
                report.add(CheckResult(
                    name="audio_loudness_lufs",
                    layer=2, passed=lufs_ok,
                    expected=f"{target_lufs} LUFS ± 3dB",
                    actual=f"{actual_lufs_f:.1f} LUFS",
                    severity="warning",
                ))

            true_peak = loudness.get("input_tp")
            target_peak = _pick(qp_profile, "audio.truePeakDbTP")
            if target_peak and true_peak:
                tp_f = float(true_peak)
                tp_ok = tp_f <= target_peak + 0.5
                report.add(CheckResult(
                    name="audio_true_peak",
                    layer=2, passed=tp_ok,
                    expected=f"≤ {target_peak} dBTP",
                    actual=f"{tp_f:.1f} dBTP",
                    severity="warning",
                ))

    # ── 2.4 Visual consistency between consecutive shots in same scene ──
    scenes = sorted(
        instance.get("production", {}).get("scenes", []),
        key=lambda s: s.get("sceneNumber", 0),
    )
    for scene in scenes:
        scene_shots = scene.get("shotRefs", [])
        if len(scene_shots) < 2:
            continue

        sid_prev = scene_shots[0].get("id", "")
        for i in range(1, len(scene_shots)):
            sid_curr = scene_shots[i].get("id", "")
            path_prev = shots_dir / f"{sid_prev}.mp4"
            path_curr = shots_dir / f"{sid_curr}.mp4"

            if path_prev.exists() and path_curr.exists():
                # Extract last frame of prev, first frame of curr
                with tempfile.TemporaryDirectory() as tmpd:
                    prev_dur = _ffprobe_duration(path_prev) or 1.0
                    frame_prev = Path(tmpd) / "prev.png"
                    frame_curr = Path(tmpd) / "curr.png"

                    _extract_frame_at(path_prev, max(0, prev_dur - 0.1), frame_prev)
                    _extract_frame_at(path_curr, 0.1, frame_curr)

                    if frame_prev.exists() and frame_curr.exists():
                        ssim = _compute_ssim(frame_prev, frame_curr)
                        if ssim is not None:
                            # Within a scene, consecutive shots should have
                            # some visual continuity (but cuts are expected)
                            report.add(CheckResult(
                                name=f"intra_scene_ssim:{sid_prev}→{sid_curr}",
                                layer=2, passed=True,  # informational
                                expected="visual continuity",
                                actual=f"SSIM={ssim:.4f}",
                                severity="info",
                            ))

            sid_prev = sid_curr


