def assess(
    instance: dict,
    output_dir: Path,
    *,
    max_layer: int = 3,
    instance_path: str = "",
) -> AssessmentReport:
    """Run assessment layers 1 through max_layer and return the report."""
    report = AssessmentReport(
        instance_path=instance_path,
        output_dir=str(output_dir),
        timestamp=datetime.now(timezone.utc).isoformat(),
    )
    t0 = time.perf_counter()

    if max_layer >= 1:
        report.layers_run.append(1)
        layer_1_technical(instance, output_dir, report)

    if max_layer >= 2:
        report.layers_run.append(2)
        layer_2_content(instance, output_dir, report)

    if max_layer >= 3:
        report.layers_run.append(3)
        layer_3_ai(instance, output_dir, report)

    report.elapsed_sec = round(time.perf_counter() - t0, 2)
    return report


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        prog="pipeline.assess",
        description="Assess generated video against its GVPP v3 spec.",
    )
    parser.add_argument("instance", help="Path to GVPP v3 instance JSON")
    parser.add_argument("output_dir", help="Path to render output directory")
    parser.add_argument(
        "--layer", type=int, default=3, choices=[1, 2, 3],
        help="Maximum assessment layer (1=technical, 2=content, 3=AI). Default: 3",
    )
    parser.add_argument(
        "-o", "--output", metavar="PATH",
        help="Write JSON report to this path",
    )
    args = parser.parse_args(argv)

    instance_path = Path(args.instance)
    if not instance_path.exists():
        print(f"Error: instance file not found: {instance_path}", file=sys.stderr)
        return 1

    output_dir = Path(args.output_dir)
    if not output_dir.is_dir():
        print(f"Error: output directory not found: {output_dir}", file=sys.stderr)
        return 1

    instance = json.loads(instance_path.read_text(encoding="utf-8"))
    report = assess(
        instance, output_dir,
        max_layer=args.layer,
        instance_path=str(instance_path),
    )

    _print_report(report)

    if args.output:
        report_path = Path(args.output)
        report_path.write_text(
            json.dumps(report.to_dict(), indent=2, ensure_ascii=False),
            encoding="utf-8",
        )
        print(f"  Report saved to: {report_path}")

    return 0 if report.failed == 0 else 1


if __name__ == "__main__":
    sys.exit(main())
